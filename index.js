const cards = (["Lisa", "Kaitlin", "Jan"]);
 
function writeCards(cards, event) {
  let result = []
  for (let i = 0; i < cards.length; i++) {
    result.push(`Thank you, ${cards[i]}, for the wonderful ${event} gift!`)
  }
  return result
}

